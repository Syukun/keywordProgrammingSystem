package dataExtractedFromSource;

public class Method4Data {
	int modifier;
	String methodName;
	String qualifiedReturnType;
	String[] qualifiedParameterType;
	
	public Method4Data(int modifier, String methodName, String simpleReturnType, String[] simpleParameterType) {
		super();
		this.modifier = modifier;
		this.methodName = methodName;
		this.qualifiedReturnType = simpleReturnType;
		this.qualifiedParameterType = simpleParameterType;
	}
	
	
	
}
